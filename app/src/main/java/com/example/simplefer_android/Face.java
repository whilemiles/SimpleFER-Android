package com.example.simplefer_android;

public class Face {
    String expression;
    int x;
    int y;
    int width;
    int height;

    public Face(String expression, int x, int y, int width, int height) {
        this.expression = expression;
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
